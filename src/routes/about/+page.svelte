  <div class="content-section">
    <h2 class="section-title">About the Research</h2>
    <p>
      The original research paper examines how K-pop fans perceive and evaluate the English accents of K-pop idols, 
      particularly those from "inner circle" countries like the US, UK, Canada, and Australia. The researchers collected 
      and analyzed 602 comments from YouTube videos featuring K-pop idols speaking English.
    </p>
    
    <h3 class="subsection-title">Key Findings</h3>
    <ul class="findings-list">
      <li>
        <strong>Overwhelmingly Positive Sentiment:</strong> 88.1% of comments express positive sentiment toward 
        idols' English accents, with only 1.3% expressing negative sentiment.
      </li>
      <li>
        <strong>Focus on Social Attractiveness:</strong> 33.6% of comments focus on how the accent sounds "cool," 
        "sexy," or otherwise socially attractive.
      </li>
      <li>
        <strong>Emphasis on Nativeness:</strong> 30.3% of comments discuss how "native-like" the idol's accent sounds, 
        often praising accents that sound like they're from inner circle countries.
      </li>
      <li>
        <strong>Linguistic Stereotyping:</strong> Comments often associate certain accent features with positive 
        social characteristics, reinforcing linguistic stereotypes.
      </li>
      <li>
        <strong>Desire for Native-Like Pronunciation:</strong> Many comments express a desire to have pronunciation 
        like the idols, particularly those with inner circle accents.
      </li>
    </ul>
    
    <h3 class="subsection-title">Research Context</h3>
    <p>
      The research is situated within the field of World Englishes, which examines how English is used in different 
      contexts around the world. Kachru's Three Circles model categorizes English-speaking countries into:
    </p>
    <ul>
      <li>
        <strong>Inner Circle:</strong> Countries where English is the primary language (US, UK, Canada, Australia, etc.)
      </li>
      <li>
        <strong>Outer Circle:</strong> Countries where English is an official language but not the primary one (India, Singapore, etc.)
      </li>
      <li>
        <strong>Expanding Circle:</strong> Countries where English is learned as a foreign language (Korea, Japan, etc.)
      </li>
    </ul>
    <p>
      The research examines how fans perceive idols' accents from inner circle countries, and how these perceptions 
      reflect broader attitudes toward different varieties of English.
    </p>
